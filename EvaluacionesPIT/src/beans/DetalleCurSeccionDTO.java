package beans;

public class DetalleCurSeccionDTO {
	private int codcur;
	private int codseccion;
	private int codusu;
	private int estado;
	
	public int getCodcur() {
		return codcur;
	}
	public void setCodcur(int codcur) {
		this.codcur = codcur;
	}
	public int getCodseccion() {
		return codseccion;
	}
	public void setCodseccion(int codseccion) {
		this.codseccion = codseccion;
	}
	public int getCodusu() {
		return codusu;
	}
	public void setCodusu(int codusu) {
		this.codusu = codusu;
	}
	public int getEstado() {
		return estado;
	}
	public void setEstado(int estado) {
		this.estado = estado;
	}
	
	
}
